#include<iostream>
using namespace std;

int main()
{
    int num =10;
    num ==10 ? cout<<"Hello True\n" : cout<<"Hello False\n"; // THis is an example of ternary operator.

    // condition ? true : false;

    (num%2==0) ? cout<<"even" : cout<<"Odd";


    return 0;
}

